package com.example.studentmanagement;

public class Student {
    public int mssv;
    public String name;
    public String email;
    public String dob;

    public Student(int mssv, String name, String mail, String dob) {
        this.mssv = mssv;
        this.name = name;
        this.email = mail;
        this.dob = dob;
    }
}
